import gradio as gr
import joblib
import numpy as np
import pandas as pd
import plotly.express as px

# -----------------------------
# Load model and dataset
# -----------------------------
model = joblib.load("disease_prediction_model.pkl")
le = joblib.load("label_encoder.pkl")

df = pd.read_csv("Training.csv")
df = df.loc[:, ~df.columns.str.contains('^Unnamed')]

symptoms_list = list(df.drop("prognosis", axis=1).columns)

# -----------------------------
# Disease Information
# -----------------------------
disease_info = {
    "Allergy": "Allergy occurs when the immune system reacts to substances like pollen, dust, or food.",
    "Fungal infection": "Fungal infections affect skin, nails, or lungs and are caused by fungi.",
    "Chronic cholestasis": "A liver condition where bile flow is reduced or blocked.",
    "Typhoid": "A bacterial infection caused by Salmonella Typhi.",
    "Migraine": "A neurological condition causing severe headaches and nausea."
}

# -----------------------------
# Prediction Function
# -----------------------------
def predict_disease(selected_symptoms):

    vector = np.zeros(len(symptoms_list))

    for symptom in selected_symptoms:
        if symptom in symptoms_list:
            vector[symptoms_list.index(symptom)] = 1

    vector = vector.reshape(1, -1)

    probs = model.predict_proba(vector)[0]

    top3 = np.argsort(probs)[-3:][::-1]

    diseases = le.inverse_transform(top3)
    confidence = probs[top3] * 100

    # -------------------------
    # Prediction text
    # -------------------------
    result = "### 🧠 Top 3 Predicted Diseases\n\n"

    for i in range(3):
        result += f"**{i+1}. {diseases[i]}** — `{confidence[i]:.2f}%`\n\n"

    # -------------------------
    # Confidence Chart
    # -------------------------
    chart_data = pd.DataFrame({
        "Disease": diseases,
        "Confidence": np.round(confidence, 2)
    })

    fig = px.bar(
        chart_data,
        y="Disease",
        x="Confidence",
        orientation="h",
        text=chart_data["Confidence"].astype(str) + "%",
        color="Disease",
        title="Prediction Confidence"
    )

    fig.update_traces(
        textposition="outside",
        cliponaxis=False
    )

    fig.update_layout(
        paper_bgcolor="black",
        plot_bgcolor="black",
        font=dict(color="white"),
        xaxis_title="Confidence (%)",
        yaxis_title="Disease",
        showlegend=False,
        height=350,
        margin=dict(l=40, r=80, t=60, b=40)
    )

    fig.update_xaxes(
        gridcolor="gray",
        range=[0, max(chart_data["Confidence"]) * 1.15]
    )

    fig.update_yaxes(gridcolor="gray")

    # -------------------------
    # Symptom Importance
    # -------------------------
    importance = model.feature_importances_

    importance_df = pd.DataFrame({
        "Symptom": symptoms_list,
        "Importance": importance
    })

    top_symptoms = importance_df.sort_values(
        "Importance",
        ascending=False
    ).head(10)

    fig2 = px.bar(
        top_symptoms,
        x="Importance",
        y="Symptom",
        orientation="h",
        title="Key Symptoms Influencing Predictions"
    )

    fig2.update_traces(marker_color="#00BFFF")

    fig2.update_layout(
        paper_bgcolor="black",
        plot_bgcolor="black",
        font=dict(color="white"),
        height=350
    )

    fig2.update_xaxes(gridcolor="gray")
    fig2.update_yaxes(gridcolor="gray")

    # -------------------------
    # Disease Information
    # -------------------------
    info = disease_info.get(
        diseases[0],
        "No detailed description available."
    )

    info_text = f"""
### 📘 About {diseases[0]}

{info}
"""

    # -------------------------
    # Risk Level
    # -------------------------
    if confidence[0] > 60:
        risk = "🔴 High Risk"
    elif confidence[0] > 30:
        risk = "🟡 Moderate Risk"
    else:
        risk = "🟢 Low Risk"

    risk_text = f"### ⚠️ Risk Level: {risk}"

    return result, fig, fig2, info_text, risk_text


# -----------------------------
# UI Layout
# -----------------------------
with gr.Blocks(title="AI Multi-Disease Prediction System") as demo:

    gr.Markdown("# 🩺 AI Multi-Disease Prediction System")
    gr.Markdown("Search symptoms and predict the top three possible diseases using AI.")

    with gr.Row():

        # LEFT COLUMN
        with gr.Column():

            symptoms_input = gr.Dropdown(
                choices=symptoms_list,
                multiselect=True,
                label="Search and Select Symptoms"
            )

            predict_btn = gr.Button("Submit")

            confidence_chart = gr.Plot(label="Prediction Confidence Chart")

        # RIGHT COLUMN
        with gr.Column():

            result_output = gr.Markdown(label="Top 3 Predicted Diseases")

            importance_chart = gr.Plot(label="Symptom Importance")

            disease_info_output = gr.Markdown(label="Disease Information")

            risk_output = gr.Markdown(label="Risk Level")

    predict_btn.click(
        fn=predict_disease,
        inputs=symptoms_input,
        outputs=[
            result_output,
            confidence_chart,
            importance_chart,
            disease_info_output,
            risk_output
        ]
    )

demo.launch()